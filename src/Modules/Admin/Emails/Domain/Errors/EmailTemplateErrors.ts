/**
 * Errors related to email templates.
 */
export enum EmailTemplateErrors {
    /**
     * Error when the template ID is invalid.
     */
    INVALID_TEMPLATE_ID = "ET-001",
    /**
     * Error when the template is not found.
     */
    TEMPLATE_NOT_FOUND = "ET-002",
    /**
     * Error when the action is invalid.
     */
    INVALID_ACTION = "ET-003",
}